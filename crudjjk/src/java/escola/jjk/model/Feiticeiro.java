package escola.jjk.model;

/**
 *
 * @author alunos
 */
public class Feiticeiro {
    private int rgf;
    private String nome;
    private String grau;
    private String tecnica_inata;
    private String periodo_escolar;
    
    

    public int getRgf() {
        return rgf;
    }

    public void setRgf(int rgf) {
        this.rgf = rgf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getGrau() {
        return grau;
    }

    public void setGrau(String grau) {
        this.grau = grau;
    }

    public String getTecnica_inata() {
        return tecnica_inata;
    }

    public void setTecnica_inata(String tecnica_inata) {
        this.tecnica_inata = tecnica_inata;
    }

    public String getPeriodo_escolar() {
        return periodo_escolar;
    }

    public void setPeriodo_escolar(String periodo_escolar) {
        this.periodo_escolar = periodo_escolar;
    }
    
    
}
